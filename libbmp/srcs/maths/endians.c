#include <stdint.h>
#include "macros.h"

uint16_t	swap2(uint16_t x)
{
	return ((x >> 8) | (x << 8));
}

uint32_t	swap4(uint32_t x)
{
	uint32_t	n1;
	uint32_t	n2;

	n1 = ((x >> 24) & B3) | ((x << 8) & B1);
	n2 = ((x >> 8) & B2) | ((x << 24) & B0);
	return (n1 | n2);
}
